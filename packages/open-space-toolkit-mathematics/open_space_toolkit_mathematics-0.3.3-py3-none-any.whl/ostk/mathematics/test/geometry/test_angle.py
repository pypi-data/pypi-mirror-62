from ostk.mathematics import geometry

################################################################################################################################################################

Angle = geometry.Angle

################################################################################################################################################################

def test_geometry_angle ():

    angle: Angle = Angle(12.34, Angle.Unit.Degree)

    assert angle is not None
    assert angle.in_degrees() == 12.34

